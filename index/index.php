    function calculateTotalPrice()
    {
        var hotelCurrency = document.getElementById('hdHotelCurrency').value;
        var outputCurrency = (document.getElementById('ddOutputCurrency') == null ? hotelCurrency : document.getElementById('ddOutputCurrency').value);

        var totalPriceOutputCurrency = 0;
        var totalPrice = 0;
        var totalPriceWithoutSupplement = 0;


        $('td[id*="tdRoomsNumber"]').each(function()
        {
            var roomQuantity = parseInt($(this).find('select[id*="ddRoomsNumber"]').val());
            if(roomQuantity <= 0) return;

            var singlePriceOutputCurrency = parseFloat($(this).find('input[id="singlePriceOutputCurrency"]').val().replace(",", "."));
            var roomPriceOutputCurrency = singlePriceOutputCurrency * roomQuantity;
            totalPriceOutputCurrency += roomPriceOutputCurrency;

            var singlePrice = parseFloat($(this).parent().find('input[id="singlePrice"]').val().replace(",", "."));
            var roomPrice = singlePrice * roomQuantity;
            totalPrice += roomPrice;

            var isSupplement = ($(this).parent().find('input[id="isSupplement"]').val() == 'True');
            if(!isSupplement) totalPriceWithoutSupplement += roomPrice;

            var childSpans = $(this).parent().find('div[id="divChild"]');
            childSpans.each(function() {
                var childQuantity = parseInt($(this).find('select[id*="ddChildNumber"]').val());
                if( childQuantity <= 0) return;

                var singleChildPriceOutputCurrency = parseFloat($(this).find('input[id="singleChildPriceOutputCurrency"]').val().replace(",", "."));
                var childPriceOutputCurrency = singleChildPriceOutputCurrency * childQuantity * roomQuantity;
                totalPriceOutputCurrency += childPriceOutputCurrency;

                var singleChildPrice = parseFloat($(this).find('input[id="singleChildPrice"]').val().replace(",", "."));
                var childPrice = singleChildPrice * childQuantity * roomQuantity;
                totalPrice += childPrice;
            });

            if($(this).find('select[id*="ddExtraBedNumber"]') != undefined && $(this).find('select[id*="ddExtraBedNumber"]') != null)
            {
                var extraBedQuantity = parseInt($(this).find('select[id*="ddExtraBedNumber"]').val());
                if(extraBedQuantity > 0)
                {
                    var singleExtraBedPriceOutputCurrency = parseFloat($(this).find('input[id="singleExtraBedPriceOutputCurrency"]').val().replace(",", "."));
                    var roomPriceOutputCurrency = singleExtraBedPriceOutputCurrency * extraBedQuantity * roomQuantity;
                    totalPriceOutputCurrency += roomPriceOutputCurrency;

                    var singleExtraBedPrice = parseFloat($(this).parent().find('input[id="singleExtraBedPrice"]').val().replace(",", "."));
                    var roomPrice = singleExtraBedPrice * extraBedQuantity * roomQuantity;
                    totalPrice += roomPrice;
                }
            }
        });


        $('span[id*="lbTotalPrice"]').text(roundNumber(totalPriceOutputCurrency, 2) + " " + outputCurrency);
        document.getElementById('totalPriceValidate').value = totalPriceWithoutSupplement;

        if(hotelCurrency != outputCurrency)
        {
            $('span[id*="lbTotalPrice"]').text(roundNumber(totalPriceOutputCurrency, 2) + " " + outputCurrency + " (" + roundNumber(totalPrice, 2) + " " + hotelCurrency + ")");
        }
    }

