    function setDayInterval() {
        // Convert both dates to milliseconds
        var startDate_ms = $("#tbArrivalDate").datepicker( "getDate" ).getTime();
        var endDate_ms = $("#tbDepartureDate").datepicker( "getDate" ).getTime();
        // Calculate the difference in milliseconds
        var difference_ms = Math.abs(endDate_ms - startDate_ms);
        // Convert back to days
        var ONE_DAY_ms = 1000 * 60 * 60 * 24;
        dayInterval = Math.round(difference_ms/ONE_DAY_ms);

        $('#txNumNights').val(dayInterval);
    }

